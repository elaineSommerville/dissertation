class Location {
  constructor(
    id,
    name,
    address,
    type,
    uri,
    style,
    date,
    architect,
    description
  ) {
    this.id = id;
    this.name = name;
    this.address = address;
    this.type = type;
    this.uri = uri;
    this.style = style;
    this.date = date;
    this.architect = architect;
    this.description = description;
  }
}
export default Location;
